    private void jButtonOtworzActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOtworzActionPerformed
        // TODO add your handling code here:
        FileNameExtensionFilter f=new FileNameExtensionFilter("JPG & GIF", "jpg", "jpeg", "gif");
        jFileChooserOtworz.setFileFilter(f);

        jFileChooserOtworz.showOpenDialog(this);

        File file = new File(jFileChooserOtworz.getSelectedFile().getAbsolutePath());
        try {
            i = ImageIO.read(file);
            Image imageResized = i.getScaledInstance(250, 200, 
                    java.awt.Image.SCALE_SMOOTH);
            ImageIcon iconOrg = new ImageIcon(imageResized);
            
            jLabelObrazOrg.setIcon(iconOrg);
            
            i2 = ImageIO.read(file);
            Image imageResized2 = i.getScaledInstance(250, 200, 
                    java.awt.Image.SCALE_SMOOTH);
            ImageIcon iconChanged = new ImageIcon(imageResized2);
            
            jLabelObrazChanged.setIcon(iconChanged);
            
        } catch (IOException ex) {
            Logger.getLogger(OknoGlowne.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonOtworzActionPerformed

